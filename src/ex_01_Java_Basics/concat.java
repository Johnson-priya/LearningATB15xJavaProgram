package ex_01_Java_Basics;

public class concat {
    public static void main(String[] args) {
        String first_name = "Johnson";
        String last_name = "Priya";
        int a= 10;
        int b= 30;
        System.out.println(first_name + last_name + a + b);
        System.out.println(a + b + first_name + last_name);
        System.out.println(first_name + " " + last_name + " " + (a+b));

    }
}
